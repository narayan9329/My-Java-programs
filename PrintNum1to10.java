
// Write a program that prints all the numbers from 1 to 10 using a loop.



import java.util.*;
public class PrintNum1to10 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int n =1;
        while(n<=10){
            System.out.println(n);
            n++;
        }
    }
}
